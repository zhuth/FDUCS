Start:
CALL INPUT
PUSH A2
PUSH A3
POP A1
POP A0
CALL INPUT
JMP INPUTOK
INPUT:
INPUT1:
JNKB INPUT1
LD A3, 8001H
INPUT2:
JNKB INPUT2
LD A2, 8001H
RET
INPUTOK:
ST A0, 7E05H
ST A1, 7E06H
ST A2, 7E07H
ST A3, 7E08H
ADD A0, A2
ADD A1, A3
SUB A2, A2
ADD A0, #F6H
JLE LESST10
ADD A1, #1
JMP DECA
LESST10:
ADD A0, #10
DECA:
ADD A1, #F6H
JLE LESSTD10
ADD A2, #1
JMP STORE:
LESSTD10:
ADD A1, #10
STORE:
ST A0, 7E00H
ST A1, 7E01H
ST A2, 7E02H
END:
JMP END