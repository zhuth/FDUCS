Start:
CALL INPUT
PUSH A2
PUSH A3
POP A1
POP A0
CALL INPUT
JMP INPUTOK
INPUT:
INPUT1:
JNKB INPUT1
LD A3, 8001H
INPUT2:
JNKB INPUT2
LD A2, 8001H
RET
INPUTOK:
ST A0, 7E05H
ST A1, 7E06H
ST A2, 7E07H
ST A3, 7E08H
ADD A0, A2
ADD A1, A3
SUB A2, A2
ADD A0, #F6H
JLE LESST10
ADD A1, #1
JMP DECA
LESST10:
ADD A0, #10
DECA:
ADD A1, #F6H
JLE LESSTD10
ADD A2, #1
JMP STORE:
LESSTD10:
ADD A1, #10
STORE:
ST A0, 7E00H
ST A1, 7E01H
ST A2, 7E02H

; print the first number
MOV A2, #0AH
CALL PRINTC
CALL PRINTC
LD A2, 7E05H
CALL PRINTC
LD A2, 7E06H
ADD A2, #10H
CALL PRINTC

; print the second number
MOV A2, #0AH
CALL PRINTC
CALL PRINTC
LD A2, 7E07H
CALL PRINTC
LD A2, 7E08H
ADD A2, #10H
CALL PRINTC

JMP SUMMARY

SUMMARY:
MOV A2, #0AH
CALL PRINTC
CALL PRINTC
LD A2, 7E00H
CALL PRINTC
LD A2, 7E01H
LD A0, 7E02H
ADD A0, #0
JLE N2
CALL PRINTC
ADD A0, #10H
PUSH A0
POP A2
CALL PRINTC
JMP ENDPROGRAM
N2:
ADD A2, #10H
CALL PRINTC
JMP ENDPROGRAM
ENDPROGRAM:
JMP ENDPROGRAM

PRINTC:
APC:
JNPB APC
ST A2, 8002H
RET